/**
 * Twitch Plus — Localization String Catalog
 *
 * Custom i18n system (not browser.i18n) for runtime language switching.
 * Usage: t("key") returns the localized string for the current locale.
 */

// ---------------------------------------------------------------------------
// Locale state
// ---------------------------------------------------------------------------
let _tpLocale = "en";

function tpSetLocale(lang) {
    if (lang === "auto" || !lang) {
        const nav = (navigator.language || "en").toLowerCase();
        const mapped = nav.replace("-", "_");
        // Try exact match (zh_cn → zh_CN)
        if (TP_STRINGS[mapped]) { _tpLocale = mapped; return; }
        // Try case-insensitive for regional (zh_cn → zh_CN, zh_tw → zh_TW)
        const regionMap = { zh_cn: "zh_CN", zh_tw: "zh_TW" };
        if (regionMap[mapped]) { _tpLocale = regionMap[mapped]; return; }
        // Try base language (es-MX → es)
        const base = nav.split(/[-_]/)[0];
        if (TP_STRINGS[base]) { _tpLocale = base; return; }
        _tpLocale = "en";
    } else {
        _tpLocale = TP_STRINGS[lang] ? lang : "en";
    }
}

function t(key) {
    return TP_STRINGS[_tpLocale]?.[key]
        || TP_STRINGS["en"]?.[key]
        || key;
}

